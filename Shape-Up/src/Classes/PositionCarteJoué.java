package Classes;

public class PositionCarteJoué {

	private int x;
	private int y;
	
	public PositionCarteJoué(int x, int y) {
		this.setX(x);
		this.setY(y);
	}
    // Setter et getters
	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
}
